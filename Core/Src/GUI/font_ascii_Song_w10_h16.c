static u8 *FindAsciiDot16(u16 c)
{
	if(c >= 'H' && c <127){
		c -= 'H'; //将字符转换为索引
		return (u8 *)Ascii_w10_h16[c];
	}else if(c == '/'){
		return (u8 *)Ascii_w10_h16[55];
	}else{
		return (u8 *)FontNotFound;
	}
};

