static u8 *FindAsciiDot8(u16 c)
{
	if(c>='.' && c<='9')
		return (u8 *)AsciiDot8[c-'.'];
	else{
		return  (u8 *)FontNotFound;
	}

};

static u8 *FindAsciiDot24(u16 c)
{
	if(c>='.' && c<='9')
		return (u8 *)AsciiDot24[c-'.'];
	else if(c==' '){
		return (u8 *)AsciiDot24[12];
	}else
		return (u8 *)FontNotFound;
};

static u8 *FindAsciiNumber_Width9(u16 c)
{
	if(c>='0' && c<='9'){
		return (u8 *)AsciiNumber_Width9[c-'.'];
	}else if(c==' '){
		return (u8 *)AsciiNumber_Width9[12];
	}else
		return (u8 *)FontNotFound;
};

static u8 *FindAsciiNumber_Width13(u16 c)
{
	if(c>='0' && c<='9'){
		return (u8 *)AsciiNumber_Width13[c-'.'];
	}else if(c=='.'){
		return (u8 *)AsciiNumber_Width13[0];
	}else if(c==' '){
		return (u8 *)AsciiNumber_Width13[12];
	}else
		return (u8 *)FontNotFound;
};


