void lcd_init(void)					//初始化
{
	LCD_CSB(0);														
	LCD_RSTB(0);							//复位
	delay_ms(5);							//复位	
	LCD_RSTB(1);							//复位结束
	delay_ms(5);							//等待复位结束
	write_byte(0xa2,0);				//1/9偏置
	write_byte(0xc8,0);				//COM模式正常
	write_byte(0xa0,0);				//SEG左右颠倒
	write_byte(0x23,0);				//设置RR(对比度粗调)
	write_byte(0x81,0);				//设置EV(对比度微调)
	write_byte(0x27,0);				//此条件下电压配置为9V左右				
	write_byte(0xf8,0);				//配置电荷泵
	write_byte(0x00,0);				//4倍压
	write_byte(0x2f,0);				//VB、VR、VF全部使能
	dianzhen(0x00,0x00);			//RAM清零
	write_byte(0xaf,0);				//开显示
}
void dianzhen(uchar dat1,uchar dat2)
{
	uchar i,page;
	for(page=0;page<8;page++)
	{
		write_byte(0xB0|page,0);	//设置页地址（Y）
		write_byte(0x40,0);				//显示起始行
		write_byte(0x10,0);				//设置列地址高四位(X)
		write_byte(0x00,0);				//设置列地址低四位(X)	
		for (i=0;i<66;i++)
		{
			write_byte(dat1,1);
			write_byte(dat2,1);
		}
	}
}
void display_zimu(uchar line,uchar column,uchar *dat,uchar length)
{
	uchar i;
	uchar page,col_L,col_H;
	page = 0xB0 | line;
	col_L = column & 0x0f;			
	col_H = (column>>4) & 0x0f | 0x10;
	write_byte(page,0);				//设置页地址（Y）
	write_byte(0x40,0);				//显示起始行
	write_byte(col_H,0);			//设置列地址高四位(X)
	write_byte(col_L,0);			//设置列地址低四位(X)
	for(i=0;i<length;i++)
	{	
		write_byte(*dat,1);
		dat++;
	}
}
void display(void)
{
	uchar i,j;
	dianzhen(0x00,0x00);
	for(i=0;i<6;i++)				//广东力先电子
	{
		display_zimu(0,i*16,(uchar *)zimu16[i*2],16);
		display_zimu(1,i*16,(uchar *)zimu16[i*2+1],16);
	}
	delay_ms(500);
	dianzhen(0xff,0xff);		//全显
	delay_ms(500);
	dianzhen(0xff,0x00);		//竖线
	delay_ms(500);
	dianzhen(0xaa,0xaa);		//横线
	delay_ms(1000);
	dianzhen(0x55,0xaa);		//棋盘格
	delay_ms(500);
	dianzhen(0x00,0x00);
	for(j=0;j<96;j=j+8)			//8x8字符
	{
		for(i=0;i<8;i++)
		{
			display_zimu(i,j,&zimu8[i+j][0],8);
		}
	}
	delay_ms(500);
	dianzhen(0x00,0x00);
	for(j=0;j<2;j++)
	{
		for(i=0;i<8;i++)			//64X64图片 兔斯基
		{
			display_zimu(i,j*64,&zimu64[i+j*8][0],64);
		}
	}
	delay_ms(1000);
}

