void delay(void)					//几us的延时
{
	uchar i = 28;
	while(i--);
}
void delay_ms(uint ms)		//大约ms级的延时
{
	uint i,j;
	for(j=0;j<ms;j++)
		for(i=0;i<14000;i++);
}
